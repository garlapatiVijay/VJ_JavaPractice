package javabasics;

public class StringConcatenation {

	public static void main(String[] args) {

		
		int a=10;
		int b=20;
		
		
		String x = "hello";
		String y = "world";
		
		System.out.println(a+b);
		System.out.println(x+y);
		System.out.println(a+b+x+y);
		System.out.println(x+y+a+b);
		System.out.println(x+y+(a+b));
		System.out.println("the value of a:"+a);

	}

}
